package com.github.lucbui.fracktail3.dnd;

/**
 * Represents a character's ability score
 */
public enum AbilityScore {
    /**
     * Strength - how strong a character is
     */
    STRENGTH,

    /**
     * Dexterity - how nimble a character is
     */
    DEXTERITY,

    /**
     * Constitution - how healthy a character is
     */
    CONSTITUTION,

    /**
     * Intelligence - how smart knowledge a character has
     */
    INTELLIGENCE,

    /**
     * Wisdom - how much common sense a character has
     */
    WISDOM,

    /**
     * Charisma - how socially interesting your character is
     */
    CHARISMA
}
